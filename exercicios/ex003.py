# Programa que lê dois números e mostra a soma entre eles

# int() converte o valor digitado para inteiro (número sem vírgula)
# float() converte o valor digitado para real (número com vírgula)

n1 = int(input('Digite um numero:'))
n2 = int(input('Digite outro numero:'))
print('A soma entre {} e {} é igual a {}!'.format(n1,n2,n1+n2))
